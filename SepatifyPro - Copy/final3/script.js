class Lyric {
  Text = undefined;
  TimeStamp = undefined;
  constructor(Text, TimeStamp) {
    this.Text = Text;
    this.TimeStamp = TimeStamp;
  }
  get Text() {
    return this.Text;
  }
  get TimeStamp() {
    return this.TimeStamp;
  }
}

class Music {
  Name = undefined;
  Artist = undefined;
  Album = undefined;
  File_Path = undefined;
  Icon_Path = undefined;
  Lyrics = [];
  constructor(Name, Artist, Album, File_Path, Icon_Path) {
    this.Name = Name;
    this.Artist = Artist;
    this.Album = Album;
    this.File_Path = File_Path;
    this.Icon_Path = Icon_Path;
  }
  get Name() {
    return this.Name;
  }
  get Artist() {
    return this.Artist;
  }
  get Album() {
    return this.Album;
  }
  get File_Path() {
    return this.File_Path;
  }
  get Icon_Path() {
    return this.Icon_Path;
  }
}

